# In Python we do not put the type of the variable it automatiquely know the type of it 


# Assigning an integer value
#int
x = 10

# Assigning a floating-point value
#float
pi = 3.14159

# Assigning a string value
#str
name = "Alice"

# Assigning a boolean value
#bool
is_true = True

# Assigning a list value
fruits = ["apple", "banana", "cherry"]

# Assigning a tuple value
colors = ("red", "green", "blue")

# Assigning a set value
unique_numbers = {1, 2, 3, 3, 2}  # Duplicate values are removed

# Assigning a dictionary value
person = {"name": "Bob", "age": 25, "city": "New York"}


print(type(x))  # Output: <class 'int'>
print(type(pi))  # Output: <class 'float'>
print(type(name))  # Output: <class 'str'>
print(type(is_true)) # Output: <class 'bool'>
print(type(fruits)) # Output: <class 'list'>
print(type(colors)) # Output: <class 'tuple'>
print(type(unique_numbers)) # Output: <class 'set'>
print(type(person)) # Output: <class 'dict'>



