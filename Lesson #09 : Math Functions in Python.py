import math

Num=float(input("please enter a number :"))

# Square root
sqrt_result = math.sqrt(Num)
print("Square root:", sqrt_result)

# Power
power_result = pow(Num, 2)
print("Power of 2:", power_result)

# Ceiling
ceil_result = math.ceil(Num)
print("Ceiling:", ceil_result)

# Floor
floor_result = math.floor(Num)
print("Floor:", floor_result)

# Round to 1 decimal place
round_result = round(Num, 1)
print("Rounded:", round_result)
